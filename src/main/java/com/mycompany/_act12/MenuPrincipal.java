    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        try {
            // Creem el fitxer contactes.txt i l'omplem amb la informació dels contacte de l'agenda
            FileWriter fitxer_escritura = new FileWriter("clients.txt");
            for (Client client : clients) {
                fitxer_escritura.write(client.toString() + "\n"); // Per defecte el meu toString ja té un salt de línia
            }
            // Tanquem el fitxer i mostrem el missatge de confirmació
            fitxer_escritura.close();
            JOptionPane.showMessageDialog(null, "Cleints exportats correctament");
        } catch (Exception ex) {
            // Mostrem un missatge conforme no s'ha pogut realitzar l'exportació.
            JOptionPane.showMessageDialog(null, "Error a l'exportar clients" + ex.getMessage());
        }
    }//GEN-LAST:event_jButton13ActionPerformed

    // Importar clients
    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        try {
            // Obres el fitxer clients_import.txt (hauràs de crear-lo prèviament)
            BufferedReader reader = new BufferedReader(new FileReader("clients_import.txt"));
            String linea;
            while ((linea = reader.readLine()) != null) {
                // Dividir la línia en parts separades
                String[] partes = linea.split(",");

                // Obtenir els valors de cada part
                String nom = partes[0];
                String cognom = partes[1];
                String telefon = partes[2];

                // Crear un nou objete Producte y afegir-lo a la lista
                clients.add(new Client(nom, cognom, telefon));
            }
            reader.close();
        } catch (Exception e) {
            System.out.println("Error al importar el archivo de respaldo.");
        }
    }//GEN-LAST:event_jButton14ActionPerformed

