    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (client_actual == null) {
            JOptionPane.showMessageDialog(null, "Selecciona el primer o últim client per seguir");
        } else {
            // Guardem la posició en la llista del client actual
            index = clients.indexOf(client_actual);
            // Reduïm una posició per mostrar el client anterior a l'actual
            index--;
            // Mostrem la informació del client anterior a l'actual
            Client seguent_client = (Client) clients.get(index);
            this.jTextField1.setText(seguent_client.getNom());
            this.jTextField2.setText(seguent_client.getCognom());
            this.jTextField3.setText(seguent_client.getN_telefon());
            this.jTextArea1.setText(seguent_client.toString());
            // Ara el client actual serà aquest anterior
            client_actual = seguent_client;
        }
    }//GEN-LAST:event_jButton3ActionPerformed

