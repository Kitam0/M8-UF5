    // Importar productes d'un fitxer 
    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        try {
            // Obres el fitxer productes_import.txt (haurÃ s de crear-lo prÃ¨viament)
            BufferedReader reader = new BufferedReader(new FileReader("productes_import.txt"));
            String linea;
            while ((linea = reader.readLine()) != null) {
                // Dividir la lÃ­nia en parts separades
                String[] partes = linea.split(",");

                // Obtenir els valors de cada part
                String nom = partes[0];
                String categoria = partes[1];
                String subcategoria = partes[2];
                double preu = Double.parseDouble(partes[3]);

                // Crear un nou objete Producte y afegir-lo a la lista
                productes.add(new Producte(nom, categoria, subcategoria, preu));
            }
            reader.close();
        } catch (Exception e) {
            System.out.println("Error al importar el archivo de respaldo.");
        }
    }//GEN-LAST:event_jButton9ActionPerformed

