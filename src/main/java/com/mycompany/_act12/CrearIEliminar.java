    // Mostrar productes a la taula
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Definim un model per defecte per la taula
        DefaultTableModel modelDefault = new DefaultTableModel(new String[]{"NOM", "CATEGORIA", "SUBCATEGORIA", "PREU"}, productes.size());
        this.jTable2.setModel(modelDefault);
        // Obtenim les dades de la taula en qÃ¼estiÃ³
        TableModel modelDades = this.jTable2.getModel();
        // Recorrem tots els productes que tenim en la llista
        for (int i = 0; i < productes.size(); i++) {
            // Guardem cada producte en una variable per mostrar-lo en la taula
            Producte producte = productes.get(i);
            modelDades.setValueAt(producte.getNom(), i, 0);
            modelDades.setValueAt(producte.getCategoria(), i, 1);
            modelDades.setValueAt(producte.getSubcategoria(), i, 2);
            modelDades.setValueAt(producte.getPreu(), i, 3);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

